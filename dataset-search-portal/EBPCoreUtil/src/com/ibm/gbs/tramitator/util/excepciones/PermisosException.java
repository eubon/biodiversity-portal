package com.ibm.gbs.tramitator.util.excepciones;

public class PermisosException extends RuntimeException{

	public PermisosException() {
		super();
	}

	public PermisosException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public PermisosException(String arg0) {
		super(arg0);
	}

	public PermisosException(Throwable arg0) {
		super(arg0);
	}

}
